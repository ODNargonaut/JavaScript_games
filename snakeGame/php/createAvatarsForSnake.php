        $G = $this->historyAvatars[$i][$j][1];
        $B = $this->historyAvatars[$i][$j][2];

        $r = $colors[$inds[$j]][0];
        $g = $colors[$inds[$j]][1];
        $b = $colors[$inds[$j]][2];

        if ($r == $R && $b == $B && $g == $G) $num--;
      }
