  public function __construct($quantityAvatars) 
  {
    $err = "Не получилось создать каталог: ";

    $dirHead = $this->dir."head";
    $dirBody = $this->dir."body";
    $dirCorner = $this->dir."corner";
    $dirTail = $this->dir."tail";
    $dirFull = $this->dir."full";

    if (!file_exists($dirHead)   && !mkdir($dirHead,   0755, true)) echo $err.$dirHead."<br />";
    if (!file_exists($dirBody)   && !mkdir($dirBody,   0755, true)) echo $err.$dirBody."<br />";
    if (!file_exists($dirCorner) && !mkdir($dirCorner, 0755, true)) echo $err.$dirCorner."<br />";
    if (!file_exists($dirTail)   && !mkdir($dirTail,   0755, true)) echo $err.$dirTail."<br />";
    if (!file_exists($dirFull)   && !mkdir($dirFull,   0755, true)) echo $err.$dirFull."<br />";

    $this->quantityAvatars = $quantityAvatars;

    $this->main();
  }

  // 
  private function main()
  {
    for ($i = 0; $i < $this->quantityAvatars; $i++)
    {
      // случайный цвет
      $this->getColorRGB();
      
      $this->createImg("head", $this->posPixelsHead, true);
      $this->createImg("body", $this->posPixelsBody);
      $this->createImg("corner", $this->posPixelsCorner);
      $this->createImg("tail", $this->posPixelsTail);

      // Создать целый аватар
      $this->createAnEntireAvatar();

      $this->N++;
    }

    echo "<br />Кол-во уникальных аватаров: ".count($this->historyAvatars);
  }

  // 
  private function createImg($title, $posPixels, $face=false)
  {
    $img = imagecreatetruecolor($this->w, $this->h);
    
    // Задать прозрачный фон
    $this->setTransparentBackground($img);
    
    if ($face)
      $this->drawPixels($img, $this->posPixelsFace, $this->reserveColors);

    $this->drawPixels($img, $posPixels, $this->selectedClrs);

    $fname = "";
    switch($title)
    {
      case "head":   $fname = $this->fHead;   break;
      case "body":   $fname = $this->fBody;   break;
      case "corner": $fname = $this->fCorner; break;
      case "tail":   $fname = $this->fTail;   break;
    }

    $this->saveImg($img, $fname.strval($this->N).$this->format);

    imagedestroy($img);
  }

  // 
  private function drawPixels($img, $arrPos, $palette)
  {
    for ($i = 0; $i < count($arrPos); $i++)
    {
      $R = $palette[$i][0];
      $G = $palette[$i][1];
      $B = $palette[$i][2];

      $clr = imagecolorallocate($img, $R, $G, $B);

      for ($j = 0; $j < count($arrPos[$i]); $j++)
      {
        $x = $arrPos[$i][$j][0];
        $y = $arrPos[$i][$j][1];

        imagesetpixel($img, $x, $y, $clr);
      }
    }
  }

  // Прозрачный фон
  private function setTransparentBackground($img)
  {
    imagealphablending($img, false);
    $transparency = imagecolorallocatealpha($img, 0, 0, 0, 127);
    imagefill($img, 0, 0, $transparency);
    imagesavealpha($img, true);
  }

  // 
  private function createAnEntireAvatar()
  {
    $quantityBlock = 5;
    $fullBody = [$this->fHead, $this->fBody, $this->fBody, $this->fBody, $this->fTail];
    $degree = 90;

    $background = imagecreatetruecolor($this->w, $this->h * $quantityBlock);
    $this->setTransparentBackground($background);

    for ($i = 0; $i < $quantityBlock; $i++)
    {
      $img = imagerotate(imagecreatefrompng($this->dir.$fullBody[$i].$this->N.$this->format), $degree, 0);

      // Делаем прозрачный фон у изображения
      $backgroundImg = imagecolorallocatealpha($img, 0, 0, 0, 127);
      imagecolortransparent($img, $backgroundImg);

      // Копирование и наложение
      imagecopymerge($background, $img, 0, $this->h * $i, 0, 0, $this->w, $this->h, 100);

      imagedestroy($img);
    }

    imagepng($background, $this->dir.$this->fFullAvatar.$this->N.$this->format);
    
    imagedestroy($background);
  }

