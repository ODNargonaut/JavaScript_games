function traitSwichCase(condition, variable, value)
{
    switch(condition)
    {
        case 1: variable = value; break;
        case 2: variable = value; break;
        case 101: variable = value; break;
        case 102: variable = value; break;
        case 103: variable = value; break;
        case 104: variable = value; break;
        case 105: variable = value; break;
        case 106: variable = value; break;
        default: variable = value;
    }
}